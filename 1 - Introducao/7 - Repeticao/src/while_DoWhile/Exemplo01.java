package while_DoWhile;

public class Exemplo01 {
    public static void main(String[] args) {
        /*
         * Crie um programa que realizará uma contagem de 1 a 10, conforme critério do
         * laço while.
         */
        int contador = 1;

        while (contador <= 10) {
            System.out.println("Número: " + contador);
            contador++;
        }
    }
}
